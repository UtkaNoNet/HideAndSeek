package com.hideandseek;

/**
 * Состояния игры. GameManager переключает их последовательно,
 * каждое изменение состояния логируется через MessageUtil.debug(...)
 * — именно это и есть строки вида "[DEBUG] GameState -> VOTING" из ТЗ.
 */
public enum GameState {
    IDLE,           // игра не идёт, все в хабе
    ROLE_ASSIGN,    // роли только что распределены, идут тайтлы/выдача предметов
    VOTING,         // голосование за карту
    HIDING,         // прячущиеся на карте, искатель ещё в хабе (60 сек)
    SEARCHING,      // искатель телепортирован, основная фаза игры
    ENDING          // игра закончилась, идут финальные 20 секунд перед хабом
}
